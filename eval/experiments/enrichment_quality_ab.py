#!/usr/bin/env python3
"""A/B the tool-enrichment renderer against the existing (text-only) vault notes.

The cost of enrichment is measured (tool_enrichment_cost.py); this answers the
other half — is the *quality* worth it? For each engineering session whose
transcript still exists, re-curate it through the **current** production path
(render_transcript → scrub → _call_path_a) and lay the new artifact next to the
note already in the vault (generated by the pre-enrichment, text-only pipeline).

Faithful replay: same loader, same scrub, same prompt, same model transport as
production — the only changed variable vs the vault note is the renderer (plus
the small prompt tweak that ships with it).

SAFETY
------
- Default is a DRY RUN: prints the plan + per-session token deltas, NO calls.
- CAPTURE_EXPERIMENT_LIVE=1 makes real model calls (one per session) and writes
  a comparison markdown under eval/state/experiments/ (gitignored). It never
  touches the vault, log.md, or session-index.tsv.

Run:
  .venv/bin/python3 eval/experiments/enrichment_quality_ab.py            # dry run
  CAPTURE_EXPERIMENT_LIVE=1 .venv/bin/python3 eval/experiments/enrichment_quality_ab.py
"""

from __future__ import annotations

import glob
import os
import pathlib
import sys

REPO = pathlib.Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(REPO / "hooks"))

import curate  # noqa: E402
import scrub as scrub_mod  # noqa: E402

PROMPTS = REPO / "prompts"
VAULT = pathlib.Path(os.environ.get("CAPTURE_VAULT_DIR", "")) / "Inbox" / "auto"
OUT_DIR = REPO / "eval" / "state" / "experiments" / "enrichment_ab"

# Engineering sessions whose transcripts survive (personal/legal sessions excluded
# — they are out of scope for the engineering-artifact goal).
SESSIONS = [
    "13ca1e63",
    "20f24265",
    "23cf9e07",
    "272e8fdd",
    "41e06f8f",
    "f3730e22",
]


def _find(pattern: str) -> str | None:
    hits = glob.glob(os.path.expanduser(pattern))
    return hits[0] if hits else None


def _baseline_text(messages: list[dict]) -> str:
    role = {"user": "[USER]", "assistant": "[ASSISTANT]"}
    return "\n".join(
        f"{role.get(m.get('role', ''), '[UNKNOWN]')}: {m.get('content', '')}"
        for m in messages
    )


def main() -> None:
    live = os.environ.get("CAPTURE_EXPERIMENT_LIVE") == "1"
    # Subscription latency is variable; allow a longer wall than the 30s prod default.
    curate.TIMEOUT_SECONDS = int(os.environ.get("CAPTURE_EXPERIMENT_TIMEOUT", "30"))
    # Optional argv: restrict to specific session-id prefixes (e.g. to retry failures).
    sessions = sys.argv[1:] or SESSIONS
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    print(f"{'session':9} | {'base tok':>9} | {'enr tok':>9} | vault note")
    print("-" * 78)
    jobs = []
    for sid in sessions:
        tpath = _find(f"~/.claude/projects/*/{sid}*.jsonl")
        note = _find(str(VAULT / f"*{sid}.md"))
        if not tpath:
            print(f"{sid:9} | transcript missing — skip")
            continue
        msgs = curate._load_transcript(tpath)
        base = len(_baseline_text(msgs)) // 4
        enr = len(curate.render_transcript(msgs)) // 4
        note_name = pathlib.Path(note).name if note else "(none in vault)"
        print(f"{sid:9} | {base:>9} | {enr:>9} | {note_name}")
        jobs.append((sid, msgs, note))

    if not live:
        print(
            "\nDRY RUN — no model calls. Set CAPTURE_EXPERIMENT_LIVE=1 to re-curate "
            f"the {len(jobs)} sessions through the enriched path and write diffs to\n  {OUT_DIR}"
        )
        return

    print(f"\nLIVE: re-curating {len(jobs)} sessions through the enriched renderer…\n")
    for sid, msgs, note in jobs:
        raw = curate.render_transcript(msgs)
        scrubbed, _ = scrub_mod.scrub(raw)
        try:
            result = curate._call_path_a(scrubbed, PROMPTS)
        except Exception as exc:  # noqa: BLE001
            print(f"{sid}: ERROR {type(exc).__name__}: {exc}")
            continue

        old = pathlib.Path(note).read_text() if note else "(no vault note)"
        if result is None or result.get("_null"):
            new_block = "MODEL RETURNED NULL"
        else:
            new_block = (
                f"title: {result.get('title')}\n"
                f"type: {result.get('type')}\n"
                f"tags: {result.get('tags')}\n"
                f"source_links: {result.get('source_links')}\n"
                f"tokens_in={result.get('tokens_in')} tokens_out={result.get('tokens_out')} "
                f"cost_usd={result.get('cost_usd'):.4f}\n\n"
                f"{result.get('body')}"
            )
        out = OUT_DIR / f"{sid}.md"
        out.write_text(
            f"# A/B for session {sid}\n\n"
            f"## OLD (vault, text-only pipeline)\n\n{old}\n\n"
            f"## NEW (enriched renderer)\n\n{new_block}\n"
        )
        print(f"{sid}: wrote {out}")

    print(f"\nDone. Comparisons in {OUT_DIR}")


if __name__ == "__main__":
    main()
